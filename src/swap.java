//Swapping using result 
public class swap {

	public static void main(String[] args) {
		int a= 10;
		System.out.println(" a = "+a);
		int b= 20; 
		System.out.println(" b = "+b);
		
		int res;
		res=a;
		System.out.println("Result is "+res);
		
		a=b;
		
		res =b;
		System.out.println("Result is "+res);

	}

}
